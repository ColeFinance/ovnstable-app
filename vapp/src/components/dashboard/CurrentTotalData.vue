<template>
  <v-col>
    <v-card class="mt-5 card-item elevation-0">
      <v-card-text>

        <template v-if="loadingCurrentTotalData">
          <v-skeleton-loader
              class="mx-auto"
              type="list-item-two-line"
          ></v-skeleton-loader>

          <v-skeleton-loader
              class="mx-auto"
              type="list-item-two-line"
          ></v-skeleton-loader>

          <v-skeleton-loader
              class="mx-auto"
              type="list-item-two-line"
          ></v-skeleton-loader>

          <v-skeleton-loader
              class="mx-auto"
              type="list-item-two-line"
          ></v-skeleton-loader>
        </template>
        <template v-else>
          <v-container>
            <v-row dense>
              <v-col style="text-align: start">
                Active
              </v-col>

              <v-col>
                Position (# of tokens)
              </v-col>

              <v-col>
                Market price (USDC/token)
              </v-col>
              <v-col>
                Net Asset Value (USDC)
              </v-col>

              <v-col>
                Liquidation price (USDC/token)
              </v-col>

              <v-col>
                Liquidation value (USDC)
              </v-col>
            </v-row>

            <v-row dense :key="item.symbol" v-for="item in currentTotalData" class="row">

              <v-col style="text-align: start">
                {{ item.symbol }}
              </v-col>
              <v-col>
                {{ item.bookValue }}
              </v-col>
              <v-col>
                {{ item.price }}
              </v-col>

              <v-col>
                {{ item.bookPrice }}
              </v-col>
              <v-col>
                {{ item.liquidationPrice }}
              </v-col>
              <v-col>
                {{ item.liquidationValue }}
              </v-col>
            </v-row>

            <v-row dense class="row pt-10">
              <v-col lg="4" style="text-align: start">
                Total
              </v-col>
              <v-col lg="4">
                {{ netAssetValueTotal }}
              </v-col>
              <v-col lg="4">
                {{ liquidationValueTotal }}
              </v-col>
            </v-row>

            <v-row dense class="row font-weight-bold">
              <v-col lg="4" style="text-align: start">
                Total USD+ in circulation
              </v-col>
              <v-col lg="4">
                  {{ totalUsdPlus.totalSupply }}
              </v-col>
              <v-col lg="1"></v-col>
            </v-row>
          </v-container>
        </template>


      </v-card-text>
    </v-card>
  </v-col>

</template>

<script>
import {mapGetters} from "vuex";
import BigNumber from "bignumber.js";

export default {
  name: "CurrentTotalData",
  data: () => ({
    menu: false,
    tab: null,


  }),


  computed: {
    ...mapGetters("profile", ['currentTotalData', 'totalUsdPlus', 'loadingCurrentTotalData']),
    ...mapGetters("web3", ['web3']),

    liquidationValueTotal: function () {

      let value = new BigNumber(0);

      if (this.currentTotalData) {

        for (let key in this.currentTotalData) {
          let item = this.currentTotalData[key];
          if (item.liquidationValue){
            value = value.plus(new BigNumber(item.liquidationValue.replace(/[^\d.-]/g, '')))
          }
        }

      }

      return value.toFixed(6);

    },
    netAssetValueTotal: function () {

      let value = new BigNumber(0);

      if (this.currentTotalData) {

        for (let key in this.currentTotalData) {
          let item = this.currentTotalData[key];
          if (item.bookPrice){
            value = value.plus(new BigNumber(item.bookPrice.replace(/[^\d.-]/g, '')))
          }
        }

      }

      return value.toFixed(6);
    },
  },

  created() {


  },

  methods: {}
}
</script>

